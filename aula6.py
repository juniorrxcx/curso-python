# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool
# ^ uma vez que voce cria esses tipos voce nao os altera
# o Python só converte um tipo em outro se for possível
print(1 + 1)
print('a' + 'b')
print(int('1') + 1.4)
print(float('1') + 1)
print(bool(''))
print(bool('  '))
print(str(11) + 'b')